//Адаптация под экран
window.addEventListener("resize", recreateSlides); 

function recreateSlides() {
  if(window.screen.width < 360){
    cardsOnPage = 3;
  } else if (window.screen.width < 768) {
    cardsOnPage = 6;
  } else {
    cardsOnPage = 8;
  }

  p = Math.ceil(cardsArryAll.length/cardsOnPage);
  let start = (numberPage.innerHTML - 1) * cardsOnPage; //8
  let end = start + cardsOnPage; //16
  const cardsArry = cardsArryAll.slice(start, end);
  ourPetsCards.innerHTML = '';
  for (const slideData of cardsArry) {
    const slideElement = createSlide(slideData);
    ourPetsCards.appendChild(slideElement);
  };
}